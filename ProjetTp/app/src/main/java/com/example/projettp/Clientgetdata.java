package com.example.projettp;

public class Clientgetdata {
    String nom;
    String adresse;
    String tel;

    public Clientgetdata(String nom) {
        this.nom = nom;
        this.adresse = adresse;
        this.tel = tel;
    }

    public String getNom() {
        return nom;
    }

    public String getAdresse() {
        return adresse;
    }

    public String getTel() {
        return tel;
    }
}
